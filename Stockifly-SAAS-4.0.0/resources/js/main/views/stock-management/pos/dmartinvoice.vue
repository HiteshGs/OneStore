                <!-- ── SIGNATURE ── -->
                <div class="os-sig">
                    <p class="os-for">For : <strong>{{ selectedWarehouse.name?.toUpperCase() }}</strong></p>
                    <div class="os-sig-gap"></div>
                    <p class="os-auth">Authorised Signatory</p>
                    <p class="os-store-sig">{{ selectedWarehouse.name }}</p>
