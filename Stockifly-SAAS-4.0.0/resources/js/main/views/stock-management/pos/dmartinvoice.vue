                <div class="os-totals">
                    <div class="os-trow"><span>SUBTOTAL</span><span>{{ formatAmountCurrency(taxableAmount) }}</span></div>
                    <div class="os-trow gst-cgst" v-if="itemGSTDetails.cgst > 0"><span>CGST @ {{ cgstRate }}%</span><span>{{ formatAmountCurrency(cgstAmount) }}</span></div>
                    <div class="os-trow gst-sgst" v-if="itemGSTDetails.sgst > 0"><span>SGST @ {{ sgstRate }}%</span><span>{{ formatAmountCurrency(sgstAmount) }}</span></div>
                    <div class="os-trow gst-igst" v-if="itemGSTDetails.igst > 0"><span>IGST @ {{ igstRate }}%</span><span>{{ formatAmountCurrency(igstAmount) }}</span></div>
                    <div class="os-trow total-tax" v-if="totalTax > 0"><span>TOTAL TAX</span><span>{{ formatAmountCurrency(totalTax) }}</span></div>
                </div>

                <hr class="os-hr-thin" />

                <!-- ── NET AMOUNT ── -->
                <div class="os-net">
                    <span>NET AMOUNT</span>
                    <span>{{ formatAmountCurrency(order.total) }}</span>
