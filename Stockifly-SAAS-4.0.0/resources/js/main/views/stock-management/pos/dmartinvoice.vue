                <p class="os-sec-title">CUSTOMER DETAILS :</p>
                <div class="os-customer">
                    <div class="os-crow"><span class="os-cl">Name</span><span class="os-cv">: {{ customer.name }}</span></div>
                    <div class="os-crow" v-if="customer.phone"><span class="os-cl">Mob</span><span class="os-cv">: {{ customer.phone }}</span></div>
