            </div>
        </div>

        <template #footer>
            <div class="os-modal-footer">
                <div class="os-layout-pick">
                    <a-space>
                        <span>Print Layout:</span>
                        <a-select v-model:value="selectedPrintLayout" style="width:140px;" @change="onPrintLayoutChange">
                            <a-select-option value="thermal104">Thermal 104mm</a-select-option>
                            <a-select-option value="a4">A4 Paper</a-select-option>
                            <a-select-option value="a5">A5 Paper</a-select-option>
                        </a-select>
                    </a-space>
                </div>
                <div class="os-btn-row">
                    <a-button v-if="customer.email && isVerified" type="primary" @click="sendInvoiceMail(order.xid, selectedLang)" :loading="isSending">
                        <template #icon><SendOutlined /></template>
                        {{ $t("common.send_invoice") }}
                    </a-button>
                    <a-button type="default" @click="downloadInvoice">
                        <template #icon><DownloadOutlined /></template>
                        Download Invoice
                    </a-button>
                    <a-button type="primary" @click="printInvoice">
                        <template #icon><PrinterOutlined /></template>
                        {{ $t("common.print_invoice") }}
                    </a-button>
                </div>
            </div>
        </template>
    </a-modal>
