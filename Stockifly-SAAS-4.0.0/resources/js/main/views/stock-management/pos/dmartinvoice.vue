                <!-- ── TERMS ── -->
                <p class="os-sec-title">TERMS OF SALES :</p>
                <p class="os-terms">{{ selectedWarehouse.terms_condition || selectedWarehouse.invoice_terms || 'Goods once sold will be taken back or exchanged within 10 Days.' }}</p>

